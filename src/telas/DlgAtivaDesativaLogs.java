    public DlgAtivaDesativaLogs() {
        //Habilitar Bot√µes
        try {
            Session sessao = HibernateUtil.getSessionFactory().openSession();
            sessao.beginTransaction();

            org.hibernate.Query q = sessao.createQuery("SELECT tgname, tgenabled FROM pg_trigger WHERE tgname = 'tr_logs_gerais' AND tgenabled = 'D'");

            if (q.list().get(0) == null) {
                RdbAtivado.setSelected(true);
                RdbDesativado.setSelected(false);
            } else {
                RdbAtivado.setSelected(false);
                RdbDesativado.setSelected(true);
            }

        } catch (HibernateException he) {
            he.printStackTrace();
        }
    }

